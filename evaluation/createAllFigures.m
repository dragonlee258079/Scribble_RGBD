function creatAllFigures
clear,clc
%allDataset={'Bruce','Judd','NUSEF','Cerf','DUT-O','OSIE','SBU'};
allDataset={'Cerf','DUT-O','OSIE','SBU'};
allModel={'Mr-CNN','AWS','BMS','CA','HFT','ICL','IS','LG','QDCT','SDSR','eDN'};
%allModel={'Mr-CNN','3scale','2scale','1scale'};
sigma=0.01:0.005:0.13;
for datasetIdx=1:length(allDataset)
    close all
    datasetName=allDataset{datasetIdx};
    disp(datasetName);
    
    allSAUC=[];
    for modelIdx=1:length(allModel)
        modelName=allModel{modelIdx};
        load([datasetName '\' datasetName '_' modelName '_sAUC.mat']);
        allSAUC=[allSAUC,sAUC(:,1)];
    end
    
    createfigure(sigma, allSAUC, datasetName, allModel);
end


